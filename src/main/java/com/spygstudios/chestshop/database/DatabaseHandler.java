package com.spygstudios.chestshop.database;

import java.sql.Connection;
import java.sql.PreparedStatement;
import java.sql.SQLException;
import java.util.UUID;
import java.util.concurrent.CompletableFuture;

import com.spygstudios.chestshop.ChestShop;
import com.spygstudios.chestshop.enums.DatabaseType;
import com.spygstudios.chestshop.utils.FutureUtils;

public abstract class DatabaseHandler {

    protected final ChestShop plugin;
    protected final DatabaseType databaseType;
    protected Connection connection;

    public DatabaseHandler(ChestShop plugin, DatabaseType databaseType) {
        this.plugin = plugin;
        this.databaseType = databaseType;
        this.connection = null;
    }

    public abstract CompletableFuture<Boolean> initialize();

    public abstract void createTables() throws SQLException;

    public PreparedStatement prepareStatement(String sql) throws SQLException {
        return connection.prepareStatement(sql);
    }

    public PreparedStatement prepareStatement(String sql, int autoGeneratedKeys) throws SQLException {
        return connection.prepareStatement(sql, autoGeneratedKeys);
    }

    /**
     * Executes any SQL statement that doesn't return results (INSERT, UPDATE,
     * DELETE, etc.)
     * 
     * @param sql        The SQL statement to execute
     * @param parameters The parameters to bind to the statement
     * @return CompletableFuture<Boolean> indicating success or failure
     */
    public CompletableFuture<Boolean> execute(String sql, Object... parameters) {
        return FutureUtils.runTaskAsync(plugin, () -> {
            try (PreparedStatement stmt = connection.prepareStatement(sql)) {
                for (int i = 0; i < parameters.length; i++) {
                    Object param = parameters[i];
                    stmt.setObject(i + 1, param);
                }

                stmt.executeUpdate();
                return true;
            } catch (SQLException e) {
                plugin.getLogger().severe("Failed to execute SQL: " + sql + " - Error: " + e.getMessage());
                return false;
            }
        });
    }

    public void close() {
        try {
            if (connection != null && !connection.isClosed()) {
                connection.close();
                plugin.getLogger().info("Database connection closed (" + databaseType + ")");
            }
        } catch (SQLException e) {
            plugin.getLogger().severe("Errors occurred while closing the database connection: " + e.getMessage());
        }
    }
}
